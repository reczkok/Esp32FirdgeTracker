void send_info(float temp, bool door_open) {
    if (mqtt_client == NULL) return;
    if (!got_ssid) {
        ESP_LOGI(GATTS_TABLE_TAG, "data not sent, no connection to wifi");
        return;
    }
    char msg[100];
    // we need a message in format: "{temp}, {door_open as true/false}"
    sprintf(msg, "%f,%s", temp, door_open ? "true" : "false");
    // we need to get mac address as a string in format: "00:2A:E6:3E:FD:E1"
    char mac[18];
    sprintf(mac, "%02X:%02X:%02X:%02X:%02X:%02X", esp32_mac[0], esp32_mac[1], esp32_mac[2], esp32_mac[3], esp32_mac[4], esp32_mac[5]);
    char addr[100];
    sprintf(addr, "fridge_app/%s", mac);
    ESP_LOGI(GATTS_TABLE_TAG, "sending message: %s", msg);
    int id = esp_mqtt_client_publish(mqtt_client, addr, msg, 0, 0, 0);
    ESP_LOGI(GATTS_TABLE_TAG, "sent publish successful, msg_id=%d", id);
    if (id == -1) {
        if(mqtt_client != NULL) {
            esp_mqtt_client_reconnect(mqtt_client);
        }
    }
}

