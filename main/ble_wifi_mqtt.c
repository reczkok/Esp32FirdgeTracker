void led_task(void *pvParameter)
{
    while (1) {
        ESP_LOGI(GATTS_TABLE_TAG, "led_state: %d", led_state);
        if (led_state == 0) {
            gpio_set_level(LED_GPIO, 1);
            vTaskDelay(1000 / portTICK_PERIOD_MS);
        } else if (led_state == 1) {
            gpio_set_level(LED_GPIO, 1);
            vTaskDelay(500 / portTICK_PERIOD_MS);
            gpio_set_level(LED_GPIO, 0);
            vTaskDelay(500 / portTICK_PERIOD_MS);
        } else if (led_state == 2) {
            gpio_set_level(LED_GPIO, 0);
            vTaskSuspend(NULL);
        }
    }
}

